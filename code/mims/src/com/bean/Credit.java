package com.bean;

public class Credit {
	
	private String creditType;
	private float scale;
	
	public Credit(){
		
	}
	
	public String getCreditType() {
		return creditType;
	}
	public void setCreditType(String creditType) {
		this.creditType = creditType;
	}
	public float getScale() {
		return scale;
	}
	public void setScale(float scale) {
		this.scale = scale;
	}

}
